import Router from 'express';
import { 
  addUser,
  deleteUser,
  generateUserAPiKey, 
  getUsers
 } from '../controllers/userController';

const router = Router();


/**
 * @swagger
 * /users:
 *   get:
 *     summary: Retrieve a list of users
 *     responses:
 *       200:
 *         description: A list of users
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 *               items:
 *                 type: object
 *                 properties:
 *                   id:
 *                     type: integer
 *                     example: 1
 *                   name:
 *                     type: string
 *                     example: John Doe
 */

router.get('/', getUsers)


/**
 * @swagger
 * /api/v1/users/users:
 *   post:
 *     summary: Create a new user
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *                 example: John Doe
 *     responses:
 *       201:
 *         description: User created successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 id:
 *                   type: integer
 *                   example: 1
 *                 name:
 *                   type: string
 *                   example: John Doe
 */
router.post('/create', addUser);
/**
 * @swagger
 * /api/v1/users/generate-api-Key:
 *   post:
 *     summary: Create a new API key
 *     tags: [API Key Management]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               email:
 *                 type: string
 *                 example: "user@example.com"
 *     responses:
 *       200:
 *         description: The API key was successfully created
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 apiKey:
 *                   type: string
 *       400:
 *         description: Bad request
 */
router.post('/generate-api-Key',generateUserAPiKey)
router.delete('/delete', deleteUser)

export default router